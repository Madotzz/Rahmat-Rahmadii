package day1_ngoding;
public class Day16_ngoding {
    public static void main(String[] args) {
//  Berikut adalah cara membuat segitiga menggunakan perulangan for
        for (int i = 1; i < 8; i++) {
            for (int j = 1; j < i; j++) {
                System.out.print(" *");
            }
            System.out.println();

        }
    }
}
